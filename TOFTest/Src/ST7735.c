/* Function that prints a char
 * charval is ASCII-Value of char to print
 * x is x position of character
 * y is y position of character
*/
void tftSetColor(uint16_t FontColor, uint16_t BackColor)
{
	_fg = FontColor;
	_bg = BackColor;
}

void tftPrintChar(uint8_t charval, int x, int y)
{
	uint8_t i,ch,fz;
	uint16_t j;
	uint16_t temp;
	int zz;

	if(cfont.x_size < 8)
	{
		fz = cfont.x_size;
	}
	else
	{
		fz = cfont.x_size/8;
	}
	if (!_transparent)
	{
		tftSetAddrWindow(x,y,x+cfont.x_size-1,y+cfont.y_size-1);
		temp=((charval-cfont.offset)*((fz)*cfont.y_size))+4;

		for(j=0;j<((fz)*cfont.y_size);j++)
		{
			ch = cfont.font[temp];

			for(i=0;i<8;i++)
			{
				if((ch&(1<<(7-i)))!=0)
				{
					tftPushColor(_fg);
				}
				else
				{
					tftPushColor(_bg);
				}
			}
			temp++;
		}
	}
	else
	{
		temp=((charval-cfont.offset)*((fz)*cfont.y_size))+4;
		for(j=0;j<cfont.y_size;j++)
		{
			for (zz=0; zz<(fz); zz++)
			{
				ch = cfont.font[temp+zz];
				for(i=0;i<8;i++)
				{
					tftSetAddrWindow(x+i+(zz*8),y+j,x+i+(zz*8)+1,y+j+1);

					if((ch&(1<<(7-i)))!=0)
					{
						tftPushColor(_fg);
					}
				}
			}
			temp+=(fz);
		}
	}
}


/* Function that prints a rotated char
 * charval is ASII-Value of char to print
 * x is x position
 * y is y position of character
 * pos is the position of that character in a text (number of preceding characters)
 * deg is the rotation angle in degree
*/
void tftRotateChar(uint8_t charval, int x, int y, int pos, int deg)
{
	uint8_t i,j,ch,fz;
	uint16_t temp;
	int newx,newy;
	double radian = deg*0.0175;
	int zz;

	if(cfont.x_size < 8)
	{
		fz = cfont.x_size;
	}
	else
	{
	fz = cfont.x_size/8;
	temp=((charval-cfont.offset)*((fz)*cfont.y_size))+4;
	}
	for(j=0; j<cfont.y_size; j++)
	{
		for (zz=0;zz<(fz);zz++)
		{
			ch = cfont.font[temp+zz];

			for(i=0;i<8;i++)
			{
				newx=x+(((i+(zz*8)+(pos*cfont.x_size))*cos(radian))-((j)*sin(radian)));
				newy=y+(((j)*cos(radian))+((i+(zz*8)+(pos*cfont.x_size))*sin(radian)));

				tftSetAddrWindow(newx,newy,newx+1,newy+1);

				if((ch&(1<<(7-i)))!=0)
				{
					tftPushColor(_fg);
				}
				else
				{
					if (!_transparent)
					{
						tftPushColor(_bg);
					}
				}
			}
		}
		temp+=(fz);
	}
}


